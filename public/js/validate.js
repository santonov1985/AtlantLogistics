function emptyCheck(val){
	if ((val.val() == "") || (val.val() == null) || (val.val() == undefined))
		return true;
}

function notEmptyCheck(val){
	if ((val.val() != "") && (val.val() != null) && (val.val() != undefined))
		return true;
}